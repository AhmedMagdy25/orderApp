
public class User {

	private String id;
  	private String name;
  	private Integer phone;

	public void setId(String id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPhone(Integer phone) {
		this.phone = phone;
	}
  	public String getId() {
  		return id;
  	}
	public String getName() {
		return this.name;
	}
	public Integer getPhone() {
		return this.phone;
	}

}